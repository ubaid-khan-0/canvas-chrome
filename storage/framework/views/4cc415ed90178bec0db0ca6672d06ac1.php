    const searchInput = document.getElementById('blogSearchInput');
    const clearBtn = document.getElementById('clearSearch');
    const blogList = document.querySelector('.sidebar__post');

    // Show/hide clear button
    searchInput.addEventListener('input', () => {
        searchInput.value.trim() ? clearBtn.classList.remove('d-none') : clearBtn.classList.add('d-none');
    });

    // Clear search
    clearBtn.addEventListener('click', () => {
        searchInput.value = '';
        clearBtn.classList.add('d-none');
        location.reload();
    });

    // Fetch blogs
    function fetchBlogs(searchTerm) {
        if (!searchTerm) {
            location.reload();
            return;
        }

        blogList.innerHTML = '<div class="text-center py-3">Searching blogs...</div>';

        fetch(`/blogs/search?search=${encodeURIComponent(searchTerm)}`)
            .then(response => {
                if (!response.ok) throw new Error('Network response was not ok');
                return response.json();
            })
            .then(data => {
                blogList.innerHTML = '';

                if (data.blogs && data.blogs.length > 0) {
                    data.blogs.forEach(blog => {
                        const categorySlug = blog.category ? blog.category.toLowerCase().replace(/\s+/g, '-') : '';
                        blogList.innerHTML += `
                            <div class="rc__post">
                                <div class="rc__post-thumb">
                                    <a href="/blog/${blog.slug}">
                                        ${blog.thumbnail ? 
                                            `<img src="/${blog.thumbnail}" alt="${blog.title}">` : 
                                            `<img src="<?php echo e(asset('website_assets/img/gallery/default-blog.jpg')); ?>" alt="default image">`}
                                    </a>
                                </div>
                                <div class="rc__post-content">
                                    <h5 class="rc__post-title">
                                        <a href="/blog/${blog.slug}">
                                            ${blog.title.length > 50 ? blog.title.substring(0, 50) + '...' : blog.title}
                                        </a>
                                    </h5>
                                    <div class="postbox__meta">
                                        <span>
                                            <a href="/blog/category/${categorySlug}">
                                                ${blog.category || 'Uncategorized'}
                                            </a>
                                        </span>
                                        <span>
                                            ${new Date(blog.created_at).toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' })}
                                        </span>
                                    </div>
                                </div>
                            </div>
                        `;
                    });
                } else {
                    blogList.innerHTML = '<div class="text-center py-3">No blogs found matching your search.</div>';
                }
            })
            .catch(error => {
                console.error('Error:', error);
                blogList.innerHTML = '<div class="text-center py-3 text-danger">Error loading search results.</div>';
            });
    }

    // Debounce function
    function debounce(func, delay) {
        let timeout;
        return function(...args) {
            clearTimeout(timeout);
            timeout = setTimeout(() => func.apply(this, args), delay);
        };
    }

    // Trigger search on keyup with debounce
    searchInput.addEventListener('keyup', debounce(() => {
        const searchTerm = searchInput.value.trim();
        fetchBlogs(searchTerm);
    }, 500));

    // On form submit
    document.getElementById('blogSearchForm').addEventListener('submit', function(e) {
        e.preventDefault();
        fetchBlogs(searchInput.value.trim());
    });
